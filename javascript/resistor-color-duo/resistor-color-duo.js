export const decodedValue = (colors) => {
  const [color1, color2] = colors.map(c => COLORS.indexOf(c))

  return Number(color1 + '' + color2)
};

const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
